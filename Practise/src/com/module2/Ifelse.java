package com.module2;

public class Ifelse {
public static void main(String[] args) {
	
	int marks = 82;
	if(marks >= 80 && marks <= 90)
		System.out.println("A Grade");
	else if(marks >= 70 && marks <= 80)
		System.out.println("B Grade");
	else
		System.out.println("Invalid Grade");
	
}
}
